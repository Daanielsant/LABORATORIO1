    public static boolean punto(String comprovacion){
        boolean r;
        r = false;
        
        
        for(int i =0;i<comprovacion.length();i++){
            if(comprovacion.substring(i,i++).equals("."))
            {
                r=true;
                break;
            }
        }
        return r;
    }
    
    
